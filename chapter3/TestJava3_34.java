// The following program show how to use for loop.
public class TestJava3_34
{
    public static void main(String[] args)
    {
        int sum = 0;
        // For loop is use for caculate 1 to 5
        for(int i=1;i<=5;i++)
        {
            sum += i;
            System.out.println("i = "+i+", sum = "+sum);
        }
    }
}
