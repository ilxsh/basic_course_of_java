// The following program show how to use 'if...else if...else...'
public class TestJava3_26
{
    public static void main(String[] args)
    {
        int x = 1;
        if(x==1)
        {
            System.out.println("x == 1");
        }
        else if(x==2)
        {
            System.out.println("x == 2");
        }
        else if(x==3)
        {
            System.out.println("x == 3");
        }
        else
        {
            System.out.println("x > 3");
        }
    }
}
