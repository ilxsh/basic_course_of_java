// The following program show how to use for loop.
public class TestJava3_30
{
    public static void main(String[] args)
    {
        int i, sum = 0;
        // for loop use to caculate sum of 1 to 10
        for(i=0;i<=10;i++)
        {
            sum+=i;
        }
        System.out.println("1+2+...+10="+sum);
    }
}
