// The following program show two method of ++
public class TestJava3_18
{
    public static void main(String args[])
    {
        int a = 3, b = 3;
        System.out.print("a = "+a);
        System.out.println(", a++ = "+(a++)+", a= "+a);
        System.out.print("b = "+b);
        System.out.println(", ++b = "+(++b)+", b= "+b);
    }
}
