// The following show how to use 'if' statement, Only when the condition is satisfied.
public class TestJava3_23
{
    public static void main(String[] args)
    {
        int a = 6, b = 5;
        System.out.println("a = "+a+", b = "+b);
        if(a>b)
        {
            System.out.println("a - b = "+(a-b));
        }
        System.out.println("a * b = "+(a*b));
    }
}
